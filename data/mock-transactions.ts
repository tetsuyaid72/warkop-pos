const now = new Date();
const todayBase = new Date(now.getFullYear(), now.getMonth(), now.getDate(), 8, 0, 0).getTime();
const dayMs = 24 * 60 * 60 * 1000;
const hourMs = 60 * 60 * 1000;
const minuteMs = 60 * 1000;

function ts(daysAgo: number, hour: number, minute: number = 0): string {
  return new Date(todayBase - daysAgo * dayMs + hour * hourMs + minute * minuteMs).toISOString();
}

